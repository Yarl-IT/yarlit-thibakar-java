class IncrementOperators {
	public static void main(String args[]) {
		
		int x = 10;
		System.out.println(x);
		
		x = x + 1;
		System.out.println(x);
		
		x += 1;
		System.out.println(x);
		
		x++;                     // Post-increment
		System.out.println(x);
		
		++x;                    //  Pre-increment
		System.out.println(x);
		
		System.out.println(++x);
		System.out.println(x++);
		System.out.println(x);
		
	}
}